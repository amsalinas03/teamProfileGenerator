function Employee (name, id, email) {
    this.name = name,
    this.id = id,
    this.email = email
}

function Engineer (name, id, email, github) {
    this.name = name,
    this.id = id,
    this.email = email,
    this.github = github
}

function Intern (name, id, email, school) {
    this.name = name,
    this.id = id,
    this.email = email,
    this.school = school
}

function Manager (name, id, email, officeNumber) {
    this.name = name,
    this.id = id,
    this.email = email,
    this.officeNumber = officeNumber
}